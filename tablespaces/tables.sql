SELECT a.id_doc_macro,
       a.id_doc_macro_version,
       a.code_doc_macro,
       a.id_institution,
       CAST(COLLECT(to_number(id_software)) AS table_number) lst_software,
       a.id_professional,
       a.id_doc_area,
       a.id_doc_template,
       a.flg_status,
       'FILLED' arrow_status
  FROM (SELECT dmv.id_doc_macro_version,
               dm.id_doc_macro,
               dm.code_doc_macro,
               dm.id_institution,
               dms.id_software,
               dmp.id_professional,
               dmv.id_doc_area,
               dmv.id_doc_template,
               dm.flg_status
          FROM doc_macro dm
          JOIN doc_macro_soft dms
            ON dms.id_doc_macro = dm.id_doc_macro
          JOIN doc_macro_prof dmp
            ON dmp.id_doc_macro = dm.id_doc_macro
          JOIN doc_macro_version dmv
            ON dmv.id_doc_macro_version = dm.id_doc_macro_version
         WHERE dmv.flg_status IN ('A', 'D')
           AND dms.flg_status IN ('A', 'D')
           AND dmp.flg_status IN ('A', 'D')) a
 GROUP BY a.id_doc_macro,
          a.id_doc_macro_version,
          a.code_doc_macro,
          a.id_institution,
          a.id_professional,
          a.id_doc_area,
          a.id_doc_template,
          a.flg_status;
