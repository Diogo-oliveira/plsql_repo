CREATE OR REPLACE VIEW ME_MED_USA AS 
SELECT A.MEDID EMB_ID,
       I.MED_NAME_ID MED_ID,
			 I.MED_NAME,
       A.MED_MEDID_DESC MED_DESCR_FORMATED,
       A.MED_MEDID_DESC MED_DESCR,  
       '<b>'||A.MED_MEDID_DESC||'</b>' SHORT_MED_DESCR, 
			 NULL EMB_DESCR,
       NULL PRICE_PVP,
       NULL PRICE_REF,
       NULL PRICE_PENS,
      -- A.MED_NAME_SOURCE_CD,
       (SELECT MED_REF_FED_LEGEND_IND_DESC FROM RMILGND_REF_FED_LGND_DESC F WHERE F.MED_REF_FED_LEGEND_IND = A.MED_REF_FED_LEGEND_IND) OTC_DESCR,
       NULL GENERICO,
       /*PK_SYSDOMAIN.GET_DOMAIN('PRESCRIPTION_PHARM.GENERICO', DECODE(IM.GENERICO, 'S', 'Y', 'N'), 1)*/ NULL GENERICO_DESCR,
       NULL DCI_ID, NULL DCI_DESCR, 
       (SELECT GCDF FROM RDOSED_DOSE_DESC G WHERE G.GCDF = D.GCDF) FORM_FARM_ID,
       (SELECT GCDF_DESC FROM RDOSED_DOSE_DESC G WHERE G.GCDF = D.GCDF) FORM_FARM_DESCR,
       NULL TIPO_PROD_ID,
       NULL QT_DOS_COMP, NULL UNIT_DOS_COMP, 
       NULL N_UNITS, NULL QT_PER_UNIT, 
       MED_STRENGTH ||MED_STRENGTH_UOM DOSAGEM,
       NULL TITULAR_ID,
       NULL TITULAR_DESCR,
       NULL DATA_AIM,
       NULL ESTADO_ID,
       NULL ESTADO_DESCR,
       NULL DISP_ID,
       NULL DISP_DESCR,
       NULL ESTUP_ID,
       NULL ESTUP_DESCR, 
       NULL TRAT_ID,
       NULL TRAT_DESCR, 
       NULL EMB_UNIT_ID,
       NULL EMB_UNIT_DESCR,
       NULL GRUPO_HOM_ID, 
       NULL GRUPO_HOM_DESCR,
       NULL N_REGISTO,
       NULL COMPART,
			 'Y' FLG_COMERC,
			 'Y' FLG_AVAILABLE,		
			 NULL DISPO_ID,
			 NULL DISPO_DATA,	
       'USA' VERS
  FROM RMIID_MED A,
       RGCNSEQ_GCNSEQNO_MSTR D,
			 RMILGND_REF_FED_LGND_DESC F,
			 RMIDFID_ROUTED_DOSE_FORM_MED g,
       RMIRMID_ROUTED_MED h,
       RMINMID_MED_NAME i 
 WHERE A.MED_STATUS_CD = 0--ACTIVE
   AND D.GCN_SEQNO = A.GCN_SEQNO
   AND F.MED_REF_FED_LEGEND_IND = A.MED_REF_FED_LEGEND_IND
	 AND A.ROUTED_DOSAGE_FORM_MED_ID = G.ROUTED_DOSAGE_FORM_MED_ID
   AND G.ROUTED_MED_ID = H.ROUTED_MED_ID
   AND H.MED_NAME_ID = i.MED_NAME_ID;